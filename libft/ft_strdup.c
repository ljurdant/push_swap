#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s2;

	if (!(s2 = malloc(sizeof(char) * (ft_strlen((char*)s1) + 1))))
		return (0);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
