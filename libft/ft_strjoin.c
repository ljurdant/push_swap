#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		len;

	if (!s1 || !s2)
		return (0);
	len = ft_strlen(s1) + ft_strlen(s2);
	str = (char *)malloc(sizeof(*str) * (len + 1));
	if (str == NULL)
		return (NULL);
	ft_strlcpy(str, s1, (ft_strlen(str) + ft_strlen(s1) + 1));
	ft_strlcat(str, s2, (ft_strlen(str) + ft_strlen(s2) + 1));
	return (str);
}
